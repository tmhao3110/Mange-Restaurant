    private void initWebcam() {
        Dimension size = WebcamResolution.QVGA.getSize();
        webcam = Webcam.getWebcams().get(0); //0 is default webcam
        webcam.setViewSize(size);
        
        panel = new WebcamPanel(webcam);
        panel.setPreferredSize(size);
        panel.setFPSDisplayed(true);

        jPanel2.add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 470, 300));

        executor.execute(this);
    }

    @Override
    public void run() {
        do {
            try {
                Thread.sleep(100);
            } catch (InterruptedException e) {
                e.printStackTrace();
            }

            Result result = null;
            BufferedImage image = null;

            if (webcam.isOpen()) {
                if ((image = webcam.getImage()) == null) {
                    continue;
                }
            }

            LuminanceSource source = new BufferedImageLuminanceSource(image);
            BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

            try {
                result = new MultiFormatReader().decode(bitmap);
            } catch (NotFoundException e) {
                //No result...
            }

            if (result != null) {
                result_field.setText(result.getText());
                byte[] decode = Base64.getMimeDecoder().decode(result.getText());
                System.out.println(decode);
                try {
                    String resultDecode = new String(decode,"UTF-8");
                    NhanVien nhanvien = dao.FindbyID(resultDecode);
                    if(nhanvien.getMaNV().equalsIgnoreCase(resultDecode)){
                        DialogHepler.alert(this, "Đăng Nhập Thành Công!");
                        ShareHelper.USER = nhanvien;
                        new MainFrame().setVisible(true);
                        webcam.close();
                        this.dispose();                       
                    }
                } catch (UnsupportedEncodingException ex) {
                    Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);              
                }              
            }
        } while (true);
    }
